function add(graphicLayer, positions, xuhao, cgyzqy, cjl, bz, id, name) {
  const graphic = new mars3d.graphic.PolylineEntity({
    positions: positions,
    style: {
      color: "red",
      width: 6,

      opacity: 1,

      clampToGround: true
    },
    attr: {
      xuhao: xuhao,
      cgyzqy: cgyzqy,
      cjl: cjl,
      bz: bz
    },
    id: id,
    name: name,
    show: false
  })
  graphicLayer.addGraphic(graphic)
  addTableItem(graphic)
}
function addlx(graphicLayer, positions, id, name) {
  const graphic = new mars3d.graphic.PolylineEntity({
    positions: positions,
    style: {
      color: "skyblue",
      width: 3,
      opacity: 1,
      clampToGround: true
    },
    id: id,
    name: name,
    show: true
  })
  graphicLayer.addGraphic(graphic)
}
function addstart(graphicLayer, position, text) {
  const graphic = new mars3d.graphic.PointEntity({
    position: position,
    style: {
      color: "#ff0000",
      pixelSize: 10,
      outline: true,
      outlineColor: "#ffffff",
      outlineWidth: 2,
      label: {
        text: text,
        font_size: 18,
        color: "#ffffff",
        pixelOffsetY: -50,
        distanceDisplayCondition: true,
        distanceDisplayCondition_far: 200000,
        distanceDisplayCondition_near: 0
      },
      clampToGround: true
    },
    attr: { remark: "示例2" }
  })
  graphicLayer.addGraphic(graphic)
}
